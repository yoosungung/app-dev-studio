package kr.ac.jj.shared.domain.main.model.sys.user.author;

import kr.ac.jj.shared.domain.main.model.MainEntity;

/**
 * 시스템 - 사용자별 권한 Entity
 */
abstract class TbSysUserAuthorEntity extends MainEntity {

    private static final long serialVersionUID = -5822219692890008360L;

    protected String userId;
    protected String authorId;

    public String getUserId() {
        return this.userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getAuthorId() {
        return this.authorId;
    }

    public void setAuthorId(String authorId) {
        this.authorId = authorId;
    }

}
