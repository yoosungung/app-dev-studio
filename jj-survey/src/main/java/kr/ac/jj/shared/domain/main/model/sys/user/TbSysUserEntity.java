package kr.ac.jj.shared.domain.main.model.sys.user;

import java.util.Date;

import kr.ac.jj.shared.domain.main.model.MainEntity;

/**
 * 시스템 - 사용자 Entity
 */
abstract class TbSysUserEntity extends MainEntity {

    private static final long serialVersionUID = 6003428878874938838L;

    protected String userId;
    protected String loginNm;
    protected String loginPassword;
    protected Boolean useYn;
    protected String userLocale;
    protected Date passwordEndDe;
    protected Date beforeLoginDt;
    protected Date lastLoginDt;

    public String getUserId() {
        return this.userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getLoginNm() {
        return this.loginNm;
    }

    public void setLoginNm(String loginNm) {
        this.loginNm = loginNm;
    }

    public String getLoginPassword() {
        return this.loginPassword;
    }

    public void setLoginPassword(String loginPassword) {
        this.loginPassword = loginPassword;
    }

    public Boolean getUseYn() {
        return this.useYn;
    }

    public void setUseYn(Boolean useYn) {
        this.useYn = useYn;
    }

    public String getUserLocale() {
        return this.userLocale;
    }

    public void setUserLocale(String userLocale) {
        this.userLocale = userLocale;
    }

    public Date getPasswordEndDe() {
        return this.passwordEndDe;
    }

    public void setPasswordEndDe(Date passwordEndDe) {
        this.passwordEndDe = passwordEndDe;
    }

    public Date getBeforeLoginDt() {
        return this.beforeLoginDt;
    }

    public void setBeforeLoginDt(Date beforeLoginDt) {
        this.beforeLoginDt = beforeLoginDt;
    }

    public Date getLastLoginDt() {
        return this.lastLoginDt;
    }

    public void setLastLoginDt(Date lastLoginDt) {
        this.lastLoginDt = lastLoginDt;
    }

}
