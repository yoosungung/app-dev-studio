package kr.ac.jj.shared.infrastructure.framework.core.foundation.exception;

public class SysException extends BaseException {

    private static final long serialVersionUID = 5701850067466857143L;

    /**
     * 기본 생성자.
     */
    public SysException() {
        super();
    }

    /**
     * 상세메시지를 전달받는 생성자.
     *
     * @param message
     */
    public SysException(String message) {
        super(message);
    }

    /**
     * 상세메시지 및 <code>Throwable</code> 객체를 전달받는 생성자.
     *
     * @param message
     * @param cause
     */
    public SysException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * <code>Throwable</code> 객체를 전달받는 생성자.
     *
     * @param cause
     */
    public SysException(Throwable cause) {
        super(cause);
    }

}
