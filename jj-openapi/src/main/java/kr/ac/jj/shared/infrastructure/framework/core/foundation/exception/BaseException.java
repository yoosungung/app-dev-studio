package kr.ac.jj.shared.infrastructure.framework.core.foundation.exception;

public class BaseException extends RuntimeException {

    private static final long serialVersionUID = -4959831126895449455L;

    /**
     * 기본 생성자.
     */
    public BaseException() {
        super();
    }

    /**
     * 상세메시지를 전달받는 생성자.
     *
     * @param message
     */
    public BaseException(String message) {
        super(message);
    }

    /**
     * 상세메시지 및 <code>Throwable</code> 객체를 전달받는 생성자.
     *
     * @param message
     * @param cause
     */
    public BaseException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * <code>Throwable</code> 객체를 전달받는 생성자.
     *
     * @param cause
     */
    public BaseException(Throwable cause) {
        super(cause);
    }

}
